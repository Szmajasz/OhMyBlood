    static let shared = PersistenceController()

    static var preview: PersistenceController = {
        let result = PersistenceController(inMemory: true)
        let viewContext = result.container.viewContext
        for i in 0..<10 {
            let newItem = BloodPressure(context: viewContext)
            newItem.sys = Int16(120 + i * 5)
            newItem.dia = Int16(70 + i * 3)
            newItem.timestamp = Date().addingTimeInterval(Double(i) * 86400) // Add one day to each record
            newItem.left = i % 2 == 0 // Alternate between left and right hand
            newItem.hr = Int16(60 + i * 2)
            newItem.note = "Record \(i + 1)"
        }
        do {
            try viewContext.save()
        } catch {
